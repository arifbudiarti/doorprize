-- ----------------------------
-- View structure for v_doorprize
-- ----------------------------
DROP VIEW IF EXISTS `v_doorprize`;
CREATE ALGORITHM = UNDEFINED SQL SECURITY DEFINER VIEW `v_doorprize` AS SELECT
	tc_doorprize.*, 
	mt_participant.`name`, 
	mt_units.alias, 
	mt_participant.mt_id_participant AS id_participant, 
	mt_units.mt_id_units AS id_units, 
	IF(mt_participant.`type`=1, "Internal", "Eksternal") AS type,
	(case when (mt_participant.status = '1') then "PWT"
             when (mt_participant.status = '2' ) then "PWTT"
             when (mt_participant.status = '3' ) then "OUTSOURCHING"
             when (mt_participant.status = '4' ) then "RELAWAN"
             when (mt_participant.status = '5' ) then "FREELANCE"
             when (mt_participant.status = '6' ) then "EMBALASE"
        end) as status_participant
FROM
	mt_participant
	INNER JOIN
	mt_units
	ON 
		mt_participant.mt_id_units = mt_units.mt_id_units
	LEFT JOIN
	tc_doorprize
	ON 
		tc_doorprize.mt_id_participant = mt_participant.mt_id_participant
WHERE
	mt_participant.deleted_at IS NULL AND
	tc_doorprize.`status` IS NULL ;

-- ----------------------------
-- View structure for v_participant
-- ----------------------------
DROP VIEW IF EXISTS `v_participant`;
CREATE ALGORITHM = UNDEFINED SQL SECURITY DEFINER VIEW `v_participant` AS SELECT
	mt_participant.mt_id_participant, 
	mt_participant.participant_type, 
	mt_participant.`name`, 
	mt_units.units, 
	mt_units.alias,
	mt_participant.event_id_list,
	mt_participant.deleted_at,
	IF(mt_participant.`type`=1, "Internal", "Eksternal") AS type,
	(case when (mt_participant.status = '1') then "PWT"
             when (mt_participant.status = '2' ) then "PWTT"
             when (mt_participant.status = '3' ) then "OUTSOURCHING"
             when (mt_participant.status = '4' ) then "RELAWAN"
             when (mt_participant.status = '5' ) then "FREELANCE"
             when (mt_participant.status = '6' ) then "EMBALASE"
        end) as status
FROM
	mt_participant
	INNER JOIN
	mt_units
	ON 
		mt_participant.mt_id_units = mt_units.mt_id_units ;

